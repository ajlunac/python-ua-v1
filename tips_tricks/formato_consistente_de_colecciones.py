# Tip. Dar un formto consistente a diccionarios, listas o cualquier otra colección.
# en python.

# Definción de una lista en una sola linea:
mi_lista = ['Juan', 'Laura', 'Guadalupe']

# Dar un formato a nuestra lista:
mi_lista = [
    'Juan',
    'Laura',
    'Guadalupe'
]

# Dar formato a nuestra lista, pero con posible errores:
mi_lista = [
    'Juan',
    'Laura',
    'Guadalupe'
    'Pedro'
]

# Formato consistente de colecciones implica agregar una coma al final.
mi_lista = [
    'Juan',
    'Laura',
    'Guadalupe',
    'Pedro',
]

print(mi_lista)